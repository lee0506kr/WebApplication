package aa;

public class StringEx {
	public static void main(String[] args) {
		
		String name = "Ja" + "va"; 
		//String name을 선언 후에 값을 문장 ="Ja" + "va" 를 대입
		String str = name + 8.0;
		//String str을 선언 후에 갑에 대가 위에서 선언한 name을 불러오고 + 8.0을 하였습니다.
		
		System.out.println(name);
		//출력문 을 이용해서 name을  출력
		System.out.println(str);
		//출력문 을 이용해서 str을  출력
		
		
		System.out.println(7 + " "); // " " 한칸띄우기
		System.out.println(" " + 7);
		System.out.println(7+"");
		System.out.println(""+7);
		System.out.println(""+"");
		System.out.println(7 + 7 +"");
		System.out.println(""+7+7);
		
		
		
		
	}

}
